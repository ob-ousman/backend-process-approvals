    /**
     * @return int
     * @throws NonUniqueResultException
     */
    public function getNextNumber(): int
    {
        $lastNumero = $this->createQueryBuilder('e')
        ->select('e.number')
        ->orderBy('e.number', 'DESC')
        ->setMaxResults(1)
        ->getQuery()
        ->getOneOrNullResult();

        return $lastNumero ? $lastNumero['number'] + 1 : 1;
    }

